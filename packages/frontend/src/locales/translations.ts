export type Language = keyof typeof translations
export type TranslationKey = keyof typeof translations['pt-br']

export function getTranslation(lang: Language, key: TranslationKey, params?: Record<string, string>): string {
  const translation = translations[lang]?.[key] || translations['pt-br'][key] || key

  if (params) {
    return Object.entries(params).reduce((text, [key, value]) => {
      return text.replace(`{${key}}`, value)
    }, translation)
  }

  return translation
}
